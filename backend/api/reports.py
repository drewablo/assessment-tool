def _generate_housing_csv(result: AnalysisResponse) -> str:
    output = io.StringIO()
    w = csv.writer(output)

    w.writerow(["Affordable Housing Market Feasibility Analysis"])
    w.writerow(["Generated by Ministry Feasibility Tool"])
    w.writerow([])

    _write_summary(w, result)

    w.writerow(["MARKET SCORE BREAKDOWN"])
    w.writerow(["Metric", "Score (0-100)", "Rating", "Weight", "Description"])
    for metric in [
        result.feasibility_score.market_size,
        result.feasibility_score.income,
        result.feasibility_score.competition,
        result.feasibility_score.family_density,
    ]:
        if metric:
            w.writerow([metric.label, metric.score, metric.rating.title(), f"{metric.weight}%", metric.description])
    w.writerow([])

    d = result.demographics
    w.writerow(["DEMOGRAPHIC DATA"])
    w.writerow(["Metric", "Value", "Notes"])
    w.writerow(["Total Population", f"{d.total_population:,}" if d.total_population else "N/A", "ACS 5-year estimate"])
    w.writerow(["Total Households", f"{d.total_households:,}" if d.total_households else "N/A", ""])
    w.writerow(["Median Household Income", f"${d.median_household_income:,}" if d.median_household_income else "N/A", ""])
    w.writerow(["Cost-Burdened Renter Households", f"{d.ministry_target_population:,}" if d.ministry_target_population else "N/A", "Households paying >30% income on rent"])
    w.writerow(["HUD Tenant Households (Joined)", f"{d.hud_tenant_households:,}" if d.hud_tenant_households else "N/A", "Tenant aggregates joined by exact HUD_ID or tract keys"])
    w.writerow(["QCT-Designated Nearby Projects", f"{d.qct_designated_projects:,}" if d.qct_designated_projects else "0", "HUD QCT annual designations"])
    w.writerow(["DDA-Designated Nearby Projects", f"{d.dda_designated_projects:,}" if d.dda_designated_projects else "0", "HUD DDA annual designations"])
    w.writerow([])

    catchment_label = _catchment_label(result)
    w.writerow(["NEARBY LIHTC AFFORDABLE HOUSING PROJECTS"])
    w.writerow([f"LIHTC projects within {catchment_label}: {result.total_private_school_count}"])
    w.writerow([])
    if result.competitor_schools:
        w.writerow(["Project Name", "Distance (miles)", "City", "Low-Income Units", "HUD QCT", "HUD DDA", "Latitude", "Longitude"])
        for p in result.competitor_schools:
            designation_flags = (p.affiliation or "").upper()
            w.writerow([
                p.name,
                p.distance_miles,
                p.city or "",
                p.enrollment or "N/A",
                "Yes" if "QCT" in designation_flags else "No",
                "Yes" if "DDA" in designation_flags else "No",
                p.lat,
                p.lon,
            ])
    else:
        w.writerow(["No LIHTC project data available. Ensure hud_lihtc_projects.csv is present in the data directory."])
    w.writerow([])

    _write_data_notes(w, result)
    _write_stage2(w, result)
    _write_phase3_sections(w, result)
    return output.getvalue()


