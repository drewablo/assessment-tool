def _catchment_label(result: AnalysisResponse) -> str:
    if result.catchment_type == "isochrone":
        return f"{result.catchment_minutes}-minute drive catchment"
    return f"{result.radius_miles}-mile radius"


def _write_summary(w, result: AnalysisResponse) -> None:
    ministry_labels = {
        "schools": "School / Institution Name",
        "housing": "Project Name",
        "elder_care": "Facility / Project Name",
    }
    name_label = ministry_labels.get(result.ministry_type or "schools", "Project Name")
    w.writerow(["ANALYSIS SUMMARY"])
    w.writerow([name_label, result.school_name])
    w.writerow(["Address Analyzed", result.analysis_address])
    w.writerow(["County", result.county_name])
    w.writerow(["State", result.state_name])
    if result.catchment_type == "isochrone":
        w.writerow(["Catchment", f"{result.catchment_minutes}-minute drive (road-network isochrone)"])
        w.writerow(["Effective Bounding Radius (miles)", result.radius_miles])
    else:
        w.writerow(["Catchment", f"{result.radius_miles}-mile radius"])
    w.writerow(["Overall Feasibility Score", f"{result.feasibility_score.overall}/100"])
    w.writerow(["Recommendation", result.recommendation])
    w.writerow([])


def _write_trend(w, result: AnalysisResponse) -> None:
    if not result.trend:
        return
    t = result.trend
    w.writerow(["DEMOGRAPHIC TREND (2017 → 2022, county-level)"])
    w.writerow(["Period", t.period])
    w.writerow(["Overall Trend", t.trend_label])
    w.writerow(["School-Age Population Change", f"{t.school_age_pop_pct:+.1f}%" if t.school_age_pop_pct is not None else "N/A"])
    w.writerow(["Median Income Change (real)", f"{t.income_real_pct:+.1f}%" if t.income_real_pct is not None else "N/A", "Adjusted ~19% for 2017→2022 CPI"])
    w.writerow(["Families with Children Change", f"{t.families_pct:+.1f}%" if t.families_pct is not None else "N/A"])
    w.writerow([])


def _write_data_notes(w, result: AnalysisResponse) -> None:
    w.writerow(["DATA NOTES"])
    for note in result.data_notes:
        w.writerow([note])


def _write_stage2(w, result: AnalysisResponse) -> None:
    stage2 = result.feasibility_score.stage2
    if not stage2 or not stage2.components:
        return
    w.writerow([])
    w.writerow(["STAGE 2 OPERATING KPI DATASET"])
    w.writerow(["project_name", "metric_key", "metric_label", "score_0_100", "weight_pct", "readiness", "schema_version", "formula_version", "computed_at_utc"])
    for c in stage2.components:
        w.writerow([
            result.school_name,
            c.key,
            c.label,
            "" if c.score is None else c.score,
            c.weight,
            stage2.readiness,
            stage2.schema_version,
            stage2.formula_version,
            stage2.computed_at_utc or "",
        ])


def _write_phase3_sections(w, result: AnalysisResponse) -> None:
    if result.data_freshness:
        w.writerow([])
        w.writerow(["DATA FRESHNESS"])
        w.writerow(["Mode", result.data_freshness.mode])
        w.writerow(["Generated (UTC)", result.data_freshness.generated_at_utc])
        w.writerow(["Source", "Last Refreshed UTC", "Age (hours)", "Status", "Notes"])
        for src in result.data_freshness.sources:
            w.writerow([
                src.source_label,
                src.last_refreshed_utc or "N/A",
                src.freshness_hours if src.freshness_hours is not None else "N/A",
                src.status,
                src.notes or "",
            ])

    if result.benchmark_narrative:
        b = result.benchmark_narrative
        w.writerow([])
        w.writerow(["REGIONAL BENCHMARK NARRATIVE"])
        w.writerow(["Peer Cohort", b.peer_cohort])
        w.writerow(["In-State Percentile", b.in_state_percentile if b.in_state_percentile is not None else "N/A"])
        w.writerow(["National Percentile", b.national_percentile if b.national_percentile is not None else "N/A"])
        w.writerow(["Summary", b.narrative_summary])
        for m in b.nearest_comparable_markets:
            w.writerow(["Comparable", m])

    if result.board_report_pack:
        pack = result.board_report_pack
        w.writerow([])
        w.writerow(["BOARD-READY REPORT PACK"])
        w.writerow(["Executive Summary", pack.executive_summary])
        for risk in pack.key_risks:
            w.writerow(["Risk", risk])
        for opt in pack.strategic_options:
            w.writerow(["Option", opt])
        for action in pack.immediate_next_actions:
            w.writerow(["Next Action", action])
        for action in pack.action_roadmap.months_12:
            w.writerow(["Roadmap 12 Months", action])
        for action in pack.action_roadmap.months_24:
            w.writerow(["Roadmap 24 Months", action])
        for action in pack.action_roadmap.months_36:
            w.writerow(["Roadmap 36 Months", action])
        for assumption in pack.methodology_assumptions:
            w.writerow(["Methodology", assumption])
        for note in pack.confidence_notes:
            w.writerow(["Confidence Note", note])
