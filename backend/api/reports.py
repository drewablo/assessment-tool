"""
Export utilities: CSV report generation.
"""

import csv
import io
from models.schemas import AnalysisResponse


def generate_csv_report(result: AnalysisResponse) -> str:
    """Generate a CSV string from an AnalysisResponse."""
    ministry = result.ministry_type or "schools"
    if ministry == "housing":
        return _generate_housing_csv(result)
    if ministry == "elder_care":
        return _generate_elder_care_csv(result)
    return _generate_schools_csv(result)


# ---------------------------------------------------------------------------
# Schools
# ---------------------------------------------------------------------------

def _generate_schools_csv(result: AnalysisResponse) -> str:
    output = io.StringIO()
    w = csv.writer(output)

    w.writerow(["Catholic School Sustainability & Viability Analysis"])
    w.writerow(["Generated by Ministry Feasibility Tool"])
    w.writerow([])

    _write_summary(w, result)

    w.writerow(["SUSTAINABILITY SCORE BREAKDOWN"])
    w.writerow(["Metric", "Score (0-100)", "Rating", "Weight", "Description"])
    for metric in [
        result.feasibility_score.market_size,
        result.feasibility_score.income,
        result.feasibility_score.competition,
        result.feasibility_score.family_density,
    ]:
        if metric:
            w.writerow([metric.label, metric.score, metric.rating.title(), f"{metric.weight}%", metric.description])
    w.writerow([])

    d = result.demographics
    w.writerow(["DEMOGRAPHIC DATA"])
    w.writerow(["Metric", "Value", "Notes"])
    w.writerow(["Total Population", f"{d.total_population:,}" if d.total_population else "N/A", "ACS 5-year estimate"])
    w.writerow(["Population Under 18", f"{d.population_under_18:,}" if d.population_under_18 else "N/A", ""])
    _age_range = {"k5": "5–11", "k8": "5–14", "high_school": "14–17", "k12": "5–17"}
    age_label = _age_range.get(result.grade_level, "school-age")
    w.writerow([f"School-Age Population (ages {age_label})", f"{d.school_age_population:,}" if d.school_age_population else "N/A", "Grade-level scoped"])
    w.writerow(["Estimated Catholic % of Population", f"{d.estimated_catholic_pct}%" if d.estimated_catholic_pct else "N/A", "CARA state-level estimate"])
    w.writerow(["Est. Catholic School-Age Children", f"{d.estimated_catholic_school_age:,}" if d.estimated_catholic_school_age else "N/A", "School-age pop × Catholic %"])
    w.writerow(["Median Household Income", f"${d.median_household_income:,}" if d.median_household_income else "N/A", ""])
    w.writerow(["Total Households", f"{d.total_households:,}" if d.total_households else "N/A", ""])
    w.writerow(["Families with Children Under 18", f"{d.families_with_children:,}" if d.families_with_children else "N/A", ""])
    w.writerow(["Owner-Occupied Housing", f"{d.owner_occupied_pct}%" if d.owner_occupied_pct is not None else "N/A", "Proxy for community stability"])
    w.writerow([])

    catchment_label = _catchment_label(result)
    w.writerow(["COMPETITOR PRIVATE SCHOOLS"])
    w.writerow([f"Total private schools within {catchment_label}: {result.total_private_school_count}"])
    w.writerow([f"Catholic schools within {catchment_label}: {result.catholic_school_count}"])
    w.writerow([])
    if result.competitor_schools:
        w.writerow(["School Name", "Affiliation", "Distance (miles)", "City", "Enrollment", "Latitude", "Longitude"])
        for s in result.competitor_schools:
            w.writerow([s.name, s.affiliation, s.distance_miles, s.city or "", s.enrollment or "N/A", s.lat, s.lon])
    else:
        w.writerow(["No competitor school data available."])
    w.writerow([])

    _write_trend(w, result)
    _write_data_notes(w, result)
    _write_stage2(w, result)
    _write_phase3_sections(w, result)
    return output.getvalue()


